fun main()
{
    // var allows to change value. val doesn't allow allow to change value 
    var nameVar= "Tasin" 
    var age = 22
    println("Hello! My name is $nameVar. My age is $age")

    nameVar = "Rahim"
    println("Hello! My name is $nameVar. My age is $age")
    
    val piName = "Pi Value"
    val pi = 3.1416 
    println("Hello! I am $piName. My value is $pi")
}
